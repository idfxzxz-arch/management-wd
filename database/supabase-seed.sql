create table if not exists app_settings (
  setting_key text primary key,
  setting_value text not null,
  updated_at timestamptz not null default now()
);

alter table app_users drop constraint if exists app_users_role_check;
alter table app_users add constraint app_users_role_check check (role in ('Owner', 'Kepala Divisi', 'Staff', 'Magang', 'Wakil Owner', 'Developer', 'HRD'));
alter table app_users add column if not exists must_change_password boolean not null default true;
alter table app_users add column if not exists password_set_at timestamptz;

create table if not exists password_events (
  id bigint generated by default as identity primary key,
  app_user_id text references app_users(id) on delete cascade,
  email text not null,
  event text not null check (event in ('temporary_created', 'temporary_reset', 'changed_by_user')),
  created_at timestamptz not null default now()
);

alter table tasks add column if not exists submission_note text;
alter table tasks add column if not exists submission_file_url text;
alter table tasks add column if not exists submission_file_name text;
alter table tasks add column if not exists submitted_at timestamptz;
alter table tasks add column if not exists approved_at timestamptz;
alter table tasks add column if not exists approved_by text;

create table if not exists task_email_notifications (
  id bigint generated by default as identity primary key,
  task_id bigint references tasks(id) on delete cascade,
  recipient_employee_id bigint references employees(id) on delete set null,
  recipient_name text not null,
  recipient_email text not null,
  subject text not null,
  message text not null,
  status text not null default 'Queued' check (status in ('Queued', 'Sent', 'Failed')),
  error_message text,
  sent_at timestamptz,
  created_at timestamptz not null default now()
);
alter table task_email_notifications enable row level security;

alter table task_submissions add column if not exists task_description text;
alter table task_submissions add column if not exists deadline date;
alter table task_submissions add column if not exists reviewer_role text;
alter table task_submissions add column if not exists revision_count integer not null default 0;
alter table task_submissions add column if not exists revision_history jsonb not null default '[]'::jsonb;
alter table task_submissions drop constraint if exists task_submissions_status_check;
alter table task_submissions add constraint task_submissions_status_check check (status in ('Belum Dikumpulkan', 'Menunggu Review', 'Diterima', 'Revisi', 'Terlambat', 'Revisi Dikirim Ulang'));

alter table app_users add column if not exists employee_id bigint references employees(id) on delete set null;
alter table reports add column if not exists employee_id bigint references employees(id) on delete set null;
alter table weekly_reports add column if not exists employee_id bigint references employees(id) on delete set null;

alter table documents add column if not exists file_url text;
alter table documents add column if not exists file_name text;
alter table documents add column if not exists file_path text;

truncate table
  activity_logs,
  announcements,
  documents,
  meetings,
  minutes,
  reports,
  weekly_reports,
  sops,
  app_settings,
  password_events,
  task_email_notifications,
  task_submissions,
  tasks,
  employees,
  divisions,
  app_users
restart identity cascade;

